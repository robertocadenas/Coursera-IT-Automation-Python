my_list = ["this", "is", "a", "list"]

# len
print(len(my_list))

# im
print("im" in my_list)

# indexing
print(my_list[1])

# SEQUENCES OF DATA
my_new_list = ["this", "is", "a", "list", "of", "strings"]

# for loop
for string in my_new_list:
    print(string)

# in operator
print("a" in my_new_list)

# len
print(len(my_new_list))

# plus operator
my_list = ["this", "is", "a", "list"]
my_list = my_list + ["of", "strings"]
print(my_list)


